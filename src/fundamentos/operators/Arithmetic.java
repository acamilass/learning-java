package fundamentos.operators;

public class Arithmetic {
    public static void main(String[] args) {
        int x = 12;
        int y = 3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        System.out.println(8 % 3);
    }
}
